# Program to show for loops in  python

import math
for x in range(0,360,30):
    print(math.cos(x))

    for i in range(10,0,-1):
        print(i)

for x in range(-180,180,30):
    print(math.sin(x))
    print(math.cos(x))
    print(math.tan(x))


